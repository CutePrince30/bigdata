package com.unisk.ad.ssp.model;

public class MainJsParam {

	private String host;

	public MainJsParam() {}

	public MainJsParam(String host) {
		this.host = host;
	}

	/**
	 * @return the host
	 */
	public String getHost() {
		return host;
	}

	/**
	 * @param host
	 *            the host to set
	 */
	public void setHost(String host) {
		this.host = host;
	}

}
